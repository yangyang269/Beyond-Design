#include <iostream>

int main() {
    // Declare variables for the lengths of the rectangle
    int length = 10;  // Length of the rectangle (in units)
    int width = 14;   // Width of the rectangle (in units)

    // Calculate the area of the rectangle
    int area = length * width;

    // Print the area of the rectangle
    std::cout << "The area of the rectangle is: " << area << " square units" << std::endl;
}
